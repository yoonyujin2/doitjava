package chapter02;

public class BinaryTest {

	public static void main(String[] args) {
		int bNum = 0B1010;
		int dNum = 10;
		int oNum = 012;
		int hNum = 0xA;
		
		System.out.println(bNum);  //2진수
		System.out.println(dNum);  //10진수
		System.out.println(oNum);  //8진수
		System.out.println(hNum);  //16진수
		
	}

}
